public class Rank {
    double avg;
    double rankValue;

    public Rank(double avg) {
        this.avg = avg;
    }

    public double getAvg() {
        return avg;
    }

    public double getRankValue() {
        return rankValue;
    }

    public void setRankValue(double rankValue) {
        this.rankValue = rankValue;
    }
}
